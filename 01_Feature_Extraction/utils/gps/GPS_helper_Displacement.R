##  Total Distance Covered
#' Calculates the total distance covered (in meters), i.e. the sum of distances between two consecutively visited locations.
#' Returns a dataframe with the total distance covered (in meters), grouped by the participant IDs.
#' by Quay Au (Stachl et al., 2020), adapted by Ramona Schoedel & Fiona Kunz

locationTotalDistanceCovered = function(sensing.data) {
  #set default return value if participant has not enough GPS data, i.e. not more than two valid GPS entries
  defaultValue = NA

  # Remove duplicate rows with identical GPS coordinates & timestamps per VPN
  sensing.data = sensing.data %>% distinct(latitude, longitude, timestamp, .keep_all =TRUE)
  
  df1 = sensing.data %>% dplyr::filter(longitude != 0 | latitude != 0) %>% select(longitude, latitude)
  if(nrow(df1) <= 2){return(defaultValue)}
  res = sum(geosphere::distHaversine(cbind(df1$longitude, df1$latitude)))

  return(res)
}



## Maximum distance between two locations
#' Calculates the maximum distance between two consecutive locations (in meters), i.e. the maximum span of the visited area.
#' Returns a value with the maximum distance between two locations (in meters).
#' by Quay Au (Stachl et al., 2020), adapted by Ramona Schoedel & Fiona Kunz

locationMaximumDistance = function(sensing.data){
  #set default return value if participant has no valid GPS data
  defaultValue = NA
  
  df1 = sensing.data %>% dplyr::filter(longitude != 0 | latitude != 0) 

  if(nrow(df1) <= 1){
    res = defaultValue
    }else{
      res = max(geosphere::distm(cbind(df1$longitude, df1$latitude), fun = geosphere::distHaversine))
    }
  return(res)
}



## Standard deviation of the displacements
#' Calculates the standard deviation of the displacements (in meters), where the displacements are the distances between
#' consecutively visited locations.
#' Returns a value of the standard deviation of the displacements (in meters)
#' by Quay Au (Stachl et al., 2020), adapted by Ramona Schoedel & Fiona Kunz

locationStandardDevDisplacements = function(sensing.data){
  #set default return value if participant has not enough GPS data, i.e. not more than one valid GPS entry
  defaultValue = NA

  df1 = sensing.data %>% dplyr::filter(longitude != 0 | latitude != 0)
  
  if (nrow(df1) <= 1){
    return(defaultValue)
  }else{
      res = sd(geosphere::distHaversine(cbind(df1$longitude, df1$latitude)))
      return(res)
    }
}



## Radius of Gyration
#' Calculates the radius of gyration (in meters), i.e. the deviation from the centroid of the locations visited. The centroid is
#' defined as the centroid of the convex hull generated by the GPS coordinates of the visited locations. It is calculated with
#' the centroid function of the geosphere package, see ?geosphere::centroid. This function requires that the GPS coordinates
#' are not too close to the poles.
#' by Quay Au (Stachl et al., 2020), adapted by Ramona Schoedel & Fiona Kunz

locationRadiusGyration = function(sensing.data){
  #set default return value if participant has not enough GPS data
  defaultValue = NA
  
  df1 = sensing.data %>% dplyr::filter(longitude != 0 | latitude != 0)
  
  if(nrow(df1) <= 1){
    return(defaultValue)
  }else{
    convex_hull = chull(geosphere::mercator(cbind(df1$longitude, df1$latitude)))
    
    # if convex hull has at most 2 points, the centroid cannot be computed, it means: no motion 
    if(length(convex_hull) <= 2) return(0)
    
    c_hull_data = cbind(df1[convex_hull, ]$longitude, df1[convex_hull, ]$latitude)

    if(nrow(distinct(data.frame(round(c_hull_data, digits = 5)))) == 1) return(defaultValue)
    
    centroid = geosphere::centroid(c_hull_data)
    
    if(any(is.infinite(centroid))) return(defaultValue)
    
    distFromCentroid = geosphere::distHaversine(p1 = cbind(df1$longitude, df1$latitude), p2 = centroid)
    res = sqrt(sum(distFromCentroid^2)/nrow(df1))
    return(res)
    } 
}



## Location Variance
#' Calculates location variance feature; calculated as the logarithm of the sum of variances in latitude and longitude values.
#' Source: see Saeb et al. (2016)
#' Returns a value with the location variance
#' by Fiona Kunz

locationVariance = function(sensing.data){
  #set default return value if participant has not enough GPS data, i.e. not more than one valid GPS entry
  defaultValue = NA
  
  df1 = sensing.data %>% dplyr::filter(longitude != 0 | latitude != 0)
  
  if(nrow(df1) <= 2) {return(defaultValue)}
  
  res = log(var(df1$latitude) + var(df1$longitude))
  return(res)
}




## Calculate Spatial Coverage by Convex Hull
#' by Fiona Kunz

locationCoverageByConvexHull = function(sensing.data){
  #set default return value if participant has not enough GPS data
  defaultValue = NA
  
  df1 = sensing.data %>% dplyr::filter(longitude != 0 | latitude != 0)
  
  if(nrow(df1) == 0) return(defaultValue)
    
  # -- Calculate Convex Hull -- #
  # The algorithm is that given by Eddy (1977).
  convex_hull = chull(geosphere::mercator(cbind(df1$longitude, df1$latitude)))
  # if convex hull has at most 2 points, the area cannot be computed
  if(length(convex_hull) <= 2) return(0) #no motion: code as 0
  c_hull_data = cbind(df1[convex_hull, ]$longitude, df1[convex_hull, ]$latitude)
  
  if(nrow(distinct(data.frame(round(c_hull_data, digits = 5)))) == 1) return(defaultValue)
  
  # -- Calculate Area (in square meters) of Non-Selfintersecting Polygon-- #
  res = geosphere::areaPolygon(c_hull_data)
  return(res)
}

